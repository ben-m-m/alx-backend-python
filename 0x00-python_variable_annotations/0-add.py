#!/usr/bin/env python3
"""Adds two float numbers and returns their sum as a float.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of 'a' and 'b' as a float.
"""


def add(a: float, b: float) -> float:
    """sum"""
    return a + b
